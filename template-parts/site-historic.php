            <h3>La Placita Village's History</h3>
            <p>La Placita Village opened in 1974 with over 200,000 square feet of mixed-use space. The development was a failed attempt at an urban renewal project which ultimately displaced hundreds of barrio citizens and caused the removal of historical structures. The entire complex encompasses several buildings and structures with historical significance. Among the historical features are the Flin Building and stables, Plaza de la Mesilla, and Samaniego House.
